t = (N ** 0.5) // 1
while True:
    t += 1
    w = (t ** 2) - N
    if (w ** 0.5) % 1 == 0:
        break

p = t + w ** 0.5
q = t - w ** 0.5
f = round((p - 1) * (q - 1))
d = pow(e, -1, f)

for c in C.split():
    print(to_text(pow(int(c), d, N)), end='')
